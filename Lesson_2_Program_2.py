# Imagine that you have a list contains string of:
# list = ["1", "4", "0", "6", "9"]
# how can you sort out this list such that the result will be [0,1,4,6,9]

num_list = ["1", "4", "0", "6", "9"]

print(num_list)     # list before sort

num_list.sort()     # sort num_list

print(num_list)     # print sorted list